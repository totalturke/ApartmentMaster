// Helper function to get translations
export function t(key: string): string {
  const keys = key.split('.');
  let result: any = translations;
  
  for (const k of keys) {
    if (result[k] === undefined) {
      console.warn(`Translation key not found: ${key}`);
      return key;
    }
    result = result[k];
  }
  
  return result;
}
